// 获取所有模型列表
export const getAllModels = () => {
  return Object.entries(OFFICIAL_MODELS).map(([key, value]) => ({
    id: key,
    ...value
  }));
};

// 按分类获取模型
export const getModelsByCategory = (category: string) => {
  return Object.entries(OFFICIAL_MODELS)
    .filter(([_, model]) => model.category === category)
    .map(([key, value]) => ({
      id: key,
      ...value
    }));
};
